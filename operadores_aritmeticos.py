produto_1 = 10
produto_2 = 20

print(produto_1 + produto_2)  #Adição
print(produto_1 - produto_2)  #Subtração
print(produto_1 * produto_2)  #Multiplicação
print(produto_1 / produto_2)  #Divisão(float)
print(produto_1 // produto_2) #Divisão(int)
print(produto_1 % produto_2)  #Modulo(resto da divisão)
print(produto_1 ** produto_2) #Exponenciação


