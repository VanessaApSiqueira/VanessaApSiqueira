def sacar(valor):  #abertura do modulo
    saldo = 500

    if saldo >= valor:  # Bloco dentro do modulo
        print("valor sacado")
        print("retire seu dinheiro na boca do caixa")
    print("Obrigado por ser nosso cliente, tenha um Bom Dia")  #fechamento do bloco

sacar(500) #Fechamento do modulo